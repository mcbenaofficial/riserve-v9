from __future__ import annotations

from dataclasses import dataclass, field
from typing import Optional


@dataclass
class EvalCase:
    id: str
    inbound: str                         # customer message
    expected_intent: str                 # e.g. "booking_inquiry", "menu_question", "complaint"
    expected_tool_calls: list[str]       # tool names the agent should invoke
    expected_response_contains: list[str]  # substrings that must appear in the draft
    should_escalate: bool = False
    tags: list[str] = field(default_factory=list)
    notes: str = ""


